def search_tablets(query):
    """
    Search for text across all tablets.
    
    Args:
        query (str): Text to search for
        
    Returns:
        dict: Dictionary mapping tablet names to lists of matching lines
    """
    results = {}
    query = query.lower()
    
    for tablet_name, content in TABLET_CONTENTS.items():
        matches = []
        for line in content.split('\n'):
            if query in line.lower():
                matches.append(line.strip())
        if matches:
            results[tablet_name] = matches
    
    return results
